#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	f;
	int	s;

	f = 1;
	while (f < argc)
	{
	s = 0;
		while (argv[f][s])
		{
			write (1, &argv[f][s], 1);
	s++;
		}
		write (1, "\n", 1);
	f++;
	}
	return (0);
}
